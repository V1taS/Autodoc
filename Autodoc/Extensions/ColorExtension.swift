import UIKit

extension UIColor {
    
    static func mainRed() -> UIColor {
        return #colorLiteral(red: 0.8156862745, green: 0.007843137255, blue: 0.1058823529, alpha: 1)
    }
    
    static func mainWhite() -> UIColor {
        return #colorLiteral(red: 0.7651195526, green: 1, blue: 0.9735659957, alpha: 1)
    }
    
    static func mainDark() -> UIColor {
        return #colorLiteral(red: 0.1080824211, green: 0.1178626493, blue: 0.1263634861, alpha: 1)
    }
    
    static func mainBlue() -> UIColor {
        return #colorLiteral(red: 0, green: 0.479216218, blue: 1, alpha: 1)
    }
}
